#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	int i;

	i = 0;
	while (str && str[i] && !(str[i] == (ch)))
		i++;
	if (str && str[i] == (ch))
		return (((char*)&str[i]));
	return (NULL);
}
